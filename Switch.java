public class Switch {
    public static void main(String args[]) {

        int number = 8 ;

        switch (number) {

            case 5 : System.out.println("hello") ;
            break ;
            case 6 : System.out.println("gojo") ;
            break ;
            case 7 : System.out.println("sukuna") ;
            break ;
            default : System.out.println("itadori") ;
        }
    }
}